function writeWrapperScript(packageRoot: string): void {
  mkdirSync(COMPANION_BIN_DIR, { recursive: true });

  const questScript = join(packageRoot, "bin", "quest.ts");
  const wrapperPath = join(COMPANION_BIN_DIR, "quest");

  // Use bun to run the quest.ts script directly
  const wrapper = `#!/bin/bash
# Auto-generated by Companion server. Do not edit.
if command -v bun >/dev/null 2>&1; then
  exec bun "${questScript}" "$@"
fi

if [ -x "$HOME/.bun/bin/bun" ]; then
  export PATH="$HOME/.bun/bin:$PATH"
  exec "$HOME/.bun/bin/bun" "${questScript}" "$@"
fi

echo "quest: bun not found (looked in PATH and \$HOME/.bun/bin/bun)" >&2
exit 127
`;

  writeFileSync(wrapperPath, wrapper, "utf-8"); // sync-ok: quest setup, not called during message handling
  chmodSync(wrapperPath, 0o755); // sync-ok: quest setup, not called during message handling
}

